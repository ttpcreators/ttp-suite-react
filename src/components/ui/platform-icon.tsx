/** Libellé/texte de plateforme → clé connue (instagram/tiktok/youtube/x), ou null. */
export function platformKey(s: string | null | undefined): keyof typeof PATHS | null {
  const t = (s ?? "").toLowerCase().trim();
  if (!t) return null;
  if (t.includes("insta")) return "instagram";
  if (t.includes("tiktok") || t.includes("tik tok")) return "tiktok";
  if (t.includes("youtube") || t === "yt") return "youtube";
  if (t === "x" || t.includes("twitter") || /(^|\s)x($|\s)/.test(t)) return "x";
  return null;
}

/** Icône du réseau social (vrai logo). Rend null si la plateforme n'est pas reconnue. */
export function PlatformIcon({
  platform,
  className = "h-4 w-4",
  ...props
}: { platform: string } & Omit<SVGProps<SVGSVGElement>, "viewBox" | "fill">) {
  const key = platformKey(platform);
  if (!key) return null;
  return (
    <svg viewBox="0 0 24 24" fill="currentColor" className={className} aria-hidden {...props}>
      <path d={PATHS[key]} />
    </svg>
  );
}
